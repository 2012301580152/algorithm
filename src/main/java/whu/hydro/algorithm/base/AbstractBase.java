package whu.hydro.algorithm.base;

import org.jetbrains.annotations.NotNull;
import whu.hydro.algorithm.io.StdIn;
import whu.hydro.algorithm.io.StdOut;

import java.util.Iterator;
import java.util.NoSuchElementException;

/**
 * @ClassName AbstractBase
 * @Description TODO
 * @Author 86187
 * @Date 2019/2/18 21:38
 * @Version 1.0
 */
public abstract class AbstractBase<Item> implements Iterable<Item> {
    protected Node<Item> first;     // top of stack
    protected int N;                // size of the stack

    // helper linked list class
    protected class Node<Item>{
        protected Item item;
        protected Node<Item> next;
    }

    /**
     * Initializes an empty stack.
     */
    public AbstractBase() {
        first = null;
        N = 0;
    }

    /**
     * Returns true if this stack is empty.
     *
     * @return true if this stack is empty; false otherwise
     */
    public boolean isEmpty() {
        return first == null;
    }

    /**
     * Returns the number of items in this stack.
     *
     * @return the number of items in this stack
     */
    public int size() {
        return N;
    }

    /**
     * Returns an iterator to this stack that iterates through the items in LIFO order.
     *
     * @return an iterator to this stack that iterates through the items in LIFO order
     */
    public Iterator<Item> iterator() {
        return new ListIterator();
    }

    // an iterator, doesn't implement remove() since it's optional
    protected class ListIterator implements Iterator<Item> {

        private Node<Item> current = first;

        public boolean hasNext() {
            return current != null;
        }

        public void remove() {
            throw new UnsupportedOperationException();
        }

        public Item next() {
            if (!hasNext()) throw new NoSuchElementException();
            Item item = (Item) current.item;
            current = current.next;
            return item;
        }
    }

    /**
     * Unit tests the {@code Stack} data type.
     *
     * @param args the command-line arguments
     */
    public static void main(String[] args) {
        Stack<String> stack = new Stack<String>();
        while (!StdIn.isEmpty()) {
            String item = StdIn.readString();
            if (!item.equals("-"))
                stack.push(item);
            else if (!stack.isEmpty())
                StdOut.print(stack.pop() + " ");
        }
        StdOut.println("(" + stack.size() + " left on stack)");
    }
}


